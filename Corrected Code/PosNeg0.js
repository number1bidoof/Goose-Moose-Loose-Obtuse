let OddPos = Number(prompt("Enter a number"))

if (OddPos > 0){
    console.log("Your number is greater than 0")
} else if (OddPos < 0){
    console.log("Your number is less than 0")
} else if (OddPos === 0) {
    console.log("Your Number is 0")
} else if (OddPos = NaN) {
    console.log("THIS ISNT a NUMBER >:(")
} else {
    console.log("huh?")
}